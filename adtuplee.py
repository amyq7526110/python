#  更新”元组

#    虽然元组本身是不可变的,但是因为它同时属于容器
#    类型,也就意味着元组的某一个元素是可变的容器类
#    ,那么这个元素中的项目仍然可变

atuple = (10,20,[1,2])

atuple[-1].append[4]

print(atuple)


#   单元素元组
#   如果一个元组中只有一个元素,那么创建该元组的时
